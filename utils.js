export const addNumbers = (num1, num2) => {
    return num1 + num2;
  }
  
  export const subtractNumbers = (num1, num2) => {
    return num1 - num2;
  }
  
  export const multiplyNumbers = (num1, num2) => {
    return num1 * num2;
  }
  
  export const divideNumbers = (num1, num2) => {
    return num1 / num2 ;
  }